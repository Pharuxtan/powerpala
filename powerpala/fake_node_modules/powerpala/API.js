const Events = require('events');

module.exports = class API extends Events {
  constructor(){
    super();
    this.ready = false;
  }

  async _load(){
    try {
      if(this.startAPI instanceof Function) await this.startAPI();
      this.log('API loaded');
      this.ready = true;
    } catch (e) {
      this.error('An error occurred during initialization!', e);
    }
  }

  async _unload(){
    try {
      if(this.stopAPI instanceof Function) await this.stopAPI();
      this.ready = false;
      this.log('API unloaded');
    } catch (e) {
      this.error('An error occurred during shutting down!', e);
    }
  }

  log (...data) {
    console.log(`%c[Powerpala:API:${this.constructor.name}]`, 'color: #ff9600', ...data);
  }

  warn (...data) {
    console.warn(`%c[Powerpala:API:${this.constructor.name}]`, 'color: #ff9600', ...data);
  }

  error (...data) {
    console.error(`%c[Powerpala:API:${this.constructor.name}]`, 'color: #ff9600', ...data);
  }
}
